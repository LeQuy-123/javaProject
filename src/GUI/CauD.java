    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TranDau td = new TranDau();
        td.setMAG(listGD.get(jComboBox1.getSelectedIndex()).getMaG());
        td.setNGAYDA(jTextField1.getText());
        td.setMAT(jTextField4.getText());
        td.setDIADIEM(jTextField2.getText());
        td.setTISO(jTextField3.getText());
        td.setMADN(listDB.get(jComboBox2.getSelectedIndex()).getMaDoi());
        td.setMADK(listDB.get(jComboBox3.getSelectedIndex()).getMaDoi());
        if(td.check()) {
                gdBLL.ThemTran(td);
               JOptionPane.showMessageDialog(null, "Thành công");
        } else {
               JOptionPane.showMessageDialog(null, "Không thành công");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

