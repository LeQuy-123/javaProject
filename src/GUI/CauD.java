    private void setDoi (String mag) {
        this.jComboBox2.removeAllItems();
        this.jComboBox3.removeAllItems();
        doiTrongGiai = gdBLL.getAllDoiTrongGiai(mag);
        for(int i = 0; i < doiTrongGiai.size(); i++ ) {
            for(int j = 0; j < listDB.size(); j++) {
                if(listDB.get(j).getMaDoi().equals(doiTrongGiai.get(i))) {
                    jComboBox2.addItem(listDB.get(j).getTenDoi());
                    jComboBox3.addItem(listDB.get(j).getTenDoi());                    
                }
            }
        }
    }
